package com.masolria;

/**
 * The enum App state.For managing console interactions.
 */
public enum AppState {
    /**
     * registration/authorization app state.
     */
    ENTRY,
    /**
     * Booking actions app state.
     */
    BOOKINGS,
    /**
     * Space actions app state.
     */
    SPACES,
    /**
     * Menu choosing actions app state.
     */
    MENU,
    /**
     * Quit app state.
     */
    QUIT
}
